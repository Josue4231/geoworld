    <section class="intro">
        <h2>Objectifs du Projet</h2>
        <p>Le projet <strong>GeoWorld</strong> vise à atteindre plusieurs objectifs clés :</p>
        <ul>
            <li>
                <p><strong>Création d'une interface de consultation ergonomique :</strong>
                L'application doit offrir une interface claire et facile à naviguer permettant aux utilisateurs de consulter les données par continents, pays, capitales, langues parlées, et d'autres critères géographiques ou économiques.</p>
            </li>

            <li>
                <p><strong>Mise à jour des données :</strong>
                Étant donné que les données sont vieillissantes, l'une des tâches principales sera de créer un ou plusieurs moyens pour actualiser ces informations. Cela pourrait inclure des mises à jour automatiques via une API ou des outils permettant aux utilisateurs d'ajouter de nouvelles données manuellement.</p>
            </li>

            <li>
                <p><strong>Ajout et mise à jour de villes :</strong>
                Les utilisateurs devront pouvoir ajouter des villes à un pays spécifique ainsi que mettre à jour les données existantes, permettant une gestion active et dynamique de la base de données.</p>
            </li>
        </ul>
